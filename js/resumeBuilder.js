function insertInto(helper, replacementString) {
    return helper.replace("%data%", replacementString)
}

bio.display = function() {
    $("#header").prepend(insertInto(HTMLheaderRole, bio.role));
    $("#header").prepend(insertInto(HTMLheaderName, bio.name));

   for (contact in bio.contacts) {
        var theContact = bio.contacts[contact];
        var formattedContact = insertInto(HTMLcontactGeneric, theContact).replace("%contact%", contact);
        $("#topContacts").append(formattedContact);
        $("#footerContacts").append(formattedContact);
    }
    
    $("#header").append(insertInto(HTMLbioPic, bio.bioPic));
    $("#header").append(insertInto(HTMLwelcomeMsg, bio.welcomeMsg));

    if (bio.skills.length >0 ) {
        $("#header").append(HTMLskillsStart);
        for (skill in bio.skills) {
            theSkill = bio.skills[skill];

            $("#skills").append(insertInto(HTMLskills, theSkill));
        }
    }
}

work.display = function() {
    for (job in work.jobs) {
        var theJob = work.jobs[job];

        $("#workExperience").append(HTMLworkStart);

        var formattedEmployer = insertInto(HTMLworkEmployer, theJob.employer);
        var formattedTitle = insertInto(HTMLworkTitle, theJob.title);
        var formattedEmployerTitle = formattedEmployer + formattedTitle;
        $(".work-entry:last").append(formattedEmployerTitle);
        $(".work-entry:last").append(insertInto(HTMLworkDates, theJob.dates));
        $(".work-entry:last").append(insertInto(HTMLworkLocation, theJob.city));
        $(".work-entry:last").append(insertInto(HTMLworkDescription, theJob.description));
    }   
}

projects.display = function() {
    for (project in projects.projects) {
        var theProject = projects.projects[project];

        $("#projects").append(HTMLprojectStart);

        $(".project-entry:last").append(insertInto(HTMLprojectTitle, theProject.title));
        $(".project-entry:last").append(insertInto(HTMLprojectDates, theProject.dates));
        $(".project-entry:last").append(insertInto(HTMLprojectDescription, theProject.description));

        if (theProject.images.length > 0) {
            for (image in theProject.images) {
                var theImage = theProject.images[image];

                $(".project-entry:last").append(insertInto(HTMLprojectImage, theImage));
            }
        }
    }
}


education.display = function() {
    for (school in education.schools) {
        var theSchool = education.schools[school];

        $("#education").append(HTMLschoolStart);

        var formattedName = insertInto(HTMLschoolName, theSchool.name);
        var formattedDegree = insertInto(HTMLschoolDegree, theSchool.degree);
        formattedNameDegree = formattedName + formattedDegree;
        $(".education-entry:last").append(formattedNameDegree)
        $(".education-entry:last").append(insertInto(HTMLschoolDates, theSchool.dates));
        $(".education-entry:last").append(insertInto(HTMLschoolLocation, theSchool.city))
        
        if (theSchool.majors.length > 0) {
            for (major in theSchool.majors) {
                var theMajor= theSchool.majors[major];

                $(".education-entry:last").append(insertInto(HTMLschoolMajor, theMajor));
            }
        }
    }

    $("#education").append(HTMLonlineClasses);

    for (course in education.onlineCourses) {
        theCourse = education.onlineCourses[course];

        $("#education").append(HTMLschoolStart);

        var formattedTitle = insertInto(HTMLonlineTitle, theCourse.title);
        var formattedSchool = insertInto(HTMLonlineSchool, theCourse.school);
        var formattedTitleSchool  = formattedTitle + formattedSchool;
        $(".education-entry:last").append(formattedTitleSchool);
        $(".education-entry:last").append(insertInto(HTMLonlineDates, theCourse.dates));
        $(".education-entry:last").append(insertInto(HTMLonlineURL, theCourse.URL));
    }
}

